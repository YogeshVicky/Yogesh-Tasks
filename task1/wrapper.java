
public class wrapper {
	public static void main(String[]args) {
		int a =100;
		
		Integer x = a;                      // converting premitive (value type) datatype into reference type
		
		System.out.println(x);      
		
		int z = (int)x;                   // converting reference type into value type
		
		System.out.println(z);            
		
		
		
	}

}

















